SELECT DATE("localtime") AS DATE,
       AVG(TEMP_F) AS AVERAGE_TEMP,
       AVG(WIND_MPH) AS AVERAGE_WIND_MPH,
       AVG(PRECIP_IN) AS AVERAGE_PRECIP_IN,
       AVG(HUMIDITY) AS AVERAGE_HUMIDITY,
       AVG(CLOUDINESS) AS AVERAGE_CLOUDINESS,
       AVG(UV) AS AVERAGE_UV
FROM {{ ref("raw_sj_weather_data") }}
GROUP BY DATE